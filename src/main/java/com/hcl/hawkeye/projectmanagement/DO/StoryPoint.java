package com.hcl.hawkeye.projectmanagement.DO;

public class StoryPoint {

	private long value;
	private long text;

	/**
	 * @return the value
	 */
	public long getValue() {
		return value;
	}

	/**
	 * @param value
	 *            the value to set
	 */
	public void setValue(long value) {
		this.value = value;
	}

	/**
	 * @return the text
	 */
	public long getText() {
		return text;
	}

	/**
	 * @param text
	 *            the text to set
	 */
	public void setText(long text) {
		this.text = text;
	}

}
